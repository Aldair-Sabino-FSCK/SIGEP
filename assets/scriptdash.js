function abre(){
    document.getElementById('registro').style.display = 'inherit';
    document.getElementById('abre').style.display = 'none'
    document.getElementById('fecha').style.display = 'initial'
}
function fecha(){
    document.getElementById('registro').style.display = 'none';
    document.getElementById('abre').style.display = 'initial'
    document.getElementById('fecha').style.display = 'none'
}